            {{Indent(fields, 1)}}

            {{Indent(_documentationPrinter.TorchModuleConstructor(torchClassName), 1)}}
            public {{torchClassName}}() : base(nameof({{torchClassName}}))
            {
                {{Indent(fieldInitializers, 2)}}
            }

            {{Indent(_documentationPrinter.TorchModuleLoadWeights(), 1)}}
            public void LoadWeightsFromOnnx(string modelPath)
            {
                if (string.IsNullOrWhiteSpace(modelPath))
                {
                    throw new ArgumentException("Model path must be provided.", nameof(modelPath));
                }

                var model = Onnxify.OnnxModel.FromFile(
                    modelPath,
                    new Onnxify.OnnxModelBaseOptions
                    {
                        NodeTypeResolutionStrategy = Onnxify.NodeTypeResolutionStrategy.IgnoreIncompatible,
                    }
                );

                LoadWeightsFromOnnx(model);
            }

            {{Indent(_documentationPrinter.TorchModuleLoadWeightsFromModel(), 1)}}
            public void LoadWeightsFromOnnx(Onnxify.OnnxModel model)
            {
                if (model is null)
                {
                    throw new ArgumentNullException(nameof(model));
                }

                var tensors = model.Graph.Initializers.ToDictionary(static x => x.Name, StringComparer.Ordinal);
                {{Indent(loadCalls, 2)}}
            }

            {{Indent(_documentationPrinter.TorchModuleForward(specification, torchModule), 1)}}
            public override Tensor forward(Tensor {{torchModule.InputParameterName}})
            {
                {{Indent(forwardBody, 2)}}
            }

            {{Indent(forwardGroupTypes, 1)}}

            private static Tensor CreateShapeTensor(Tensor value)
            {
                return torch.tensor(value.shape, dtype: ScalarType.Int64, device: value.device);
            }

            private static Tensor GatherTensor(Tensor input, Tensor indices, long axis)
            {
                using var flatIndices = indices.reshape(-1);
                return input.index_select(axis, flatIndices).squeeze();
            }

            private static Tensor UnsqueezeTensor(Tensor input, Tensor axes)
            {
                return UnsqueezeTensor(input, axes.data<long>().ToArray());
            }

            private static Tensor UnsqueezeTensor(Tensor input, long[] axes)
            {
                var result = input;
                foreach (var axis in axes.OrderBy(static x => x))
                {
                    result = result.unsqueeze(axis);
                }

                return result;
            }

            private static Tensor SqueezeTensor(Tensor input, Tensor axes)
            {
                return SqueezeTensor(input, axes.data<long>().ToArray());
            }

            private static Tensor SqueezeTensor(Tensor input, long[] axes)
            {
                if (axes.Length == 0)
                {
                    return input.squeeze();
                }

                var result = input;
                foreach (var axis in NormalizeAxes(axes, input.shape.Length).OrderByDescending(static x => x))
                {
                    result = result.squeeze(axis);
                }

                return result;
            }

            private static Tensor ConcatTensors(Tensor[] tensors, long axis)
            {
                if (tensors.Length == 0)
                {
                    throw new ArgumentException("At least one tensor is required.", nameof(tensors));
                }

                var device = tensors[0].device;
                var aligned = tensors.Select(x => x.device == device ? x : x.to(device)).ToArray();
                return torch.cat(aligned, axis);
            }

            private static Tensor MaxTensors(Tensor[] tensors)
            {
                if (tensors.Length == 0)
                {
                    throw new ArgumentException("At least one tensor is required.", nameof(tensors));
                }

                var result = tensors[0];
                foreach (var tensor in tensors.Skip(1))
                {
                    result = torch.maximum(result, tensor);
                }

                return result;
            }

            private static Tensor MinTensors(Tensor[] tensors)
            {
                if (tensors.Length == 0)
                {
                    throw new ArgumentException("At least one tensor is required.", nameof(tensors));
                }

                var result = tensors[0];
                foreach (var tensor in tensors.Skip(1))
                {
                    result = torch.minimum(result, tensor);
                }

                return result;
            }

            private static Tensor ReduceMeanTensor(Tensor input, Tensor? axes, bool keepDims)
            {
                return ReduceMeanTensor(input, axes?.data<long>().ToArray(), keepDims);
            }

            private static Tensor ReduceMeanTensor(Tensor input, long[]? axes, bool keepDims)
            {
                return input.mean(ResolveReductionAxes(axes, input.shape.Length), keepDims);
            }

            private static Tensor ReduceSumTensor(Tensor input, Tensor? axes, bool keepDims)
            {
                return ReduceSumTensor(input, axes?.data<long>().ToArray(), keepDims);
            }

            private static Tensor ReduceSumTensor(Tensor input, long[]? axes, bool keepDims)
            {
                return input.sum(ResolveReductionAxes(axes, input.shape.Length), keepDims);
            }

            private static long[] ResolveReductionAxes(long[]? axes, int rank)
            {
                return axes is null || axes.Length == 0
                    ? Enumerable.Range(0, rank).Select(static x => (long)x).ToArray()
                    : NormalizeAxes(axes, rank);
            }

            private static long[] NormalizeAxes(long[] axes, int rank)
            {
                return axes
                    .Select(axis => axis < 0 ? axis + rank : axis)
                    .ToArray();
            }

            private static Tensor ToOnnxLstmY(Tensor torchY, long numDirections, long hiddenSize)
            {
                var shape = torchY.shape;
                if (shape.Length != 3)
                {
                    throw new InvalidOperationException($"TorchSharp LSTM output must have rank 3. Got rank {shape.Length}.");
                }

                return torchY
                    .reshape(new long[] { shape[0], shape[1], numDirections, hiddenSize })
                    .permute(0, 2, 1, 3);
            }

            private static Tensor QuantizeLinearTensor(Tensor input, Tensor scale, Tensor? zeroPoint, long axis)
            {
                var alignedScale = AlignQuantizationParameter(scale, input, axis);
                var scaled = input / alignedScale;
                var shifted = zeroPoint is null
                    ? scaled
                    : scaled + AlignQuantizationParameter(zeroPoint, input, axis).to(ScalarType.Float32);
                var rounded = shifted.round();
                return (zeroPoint?.dtype ?? ScalarType.Byte) switch
                {
                    ScalarType.Byte => rounded.clamp(0, 255).to(ScalarType.Byte),
                    ScalarType.Int8 => rounded.clamp(-128, 127).to(ScalarType.Int8),
                    ScalarType.Int32 => rounded.to(ScalarType.Int32),
                    _ => throw new NotSupportedException($"Unsupported QuantizeLinear zero-point tensor data type: {zeroPoint!.dtype}."),
                };
            }

            private static Tensor DequantizeLinearTensor(Tensor input, Tensor scale, Tensor? zeroPoint, long axis)
            {
                var alignedScale = AlignQuantizationParameter(scale, input, axis);
                var shifted = zeroPoint is null
                    ? input.to(ScalarType.Float32)
                    : input.to(ScalarType.Float32) - AlignQuantizationParameter(zeroPoint, input, axis).to(ScalarType.Float32);
                return shifted * alignedScale;
            }

            private static Tensor AlignQuantizationParameter(Tensor parameter, Tensor input, long axis)
            {
                var parameterShape = parameter.shape;
                if (parameterShape.Length == 0 || parameterShape.Aggregate(1L, static (product, dim) => product * dim) == 1L)
                {
                    return parameter;
                }

                var inputRank = input.shape.Length;
                var normalizedAxis = axis < 0 ? axis + inputRank : axis;
                if (normalizedAxis < 0 || normalizedAxis >= inputRank)
                {
                    throw new ArgumentOutOfRangeException(nameof(axis), axis, "Quantization axis is outside the input tensor rank.");
                }

                var shape = Enumerable.Repeat(1L, inputRank).ToArray();
                shape[normalizedAxis] = parameterShape[0];
                return parameter.reshape(shape);
            }

            private static void LoadFloatTensor(
                IReadOnlyDictionary<string, Onnxify.OnnxTensor> tensors,
                string name,
                Tensor target
            )
            {
                if (!tensors.TryGetValue(name, out var tensor))
                {
                    throw new KeyNotFoundException($"The ONNX model does not contain initializer '{name}'.");
                }

                if (tensor is not Onnxify.OnnxTensor<float> typedTensor)
                {
                    throw new InvalidOperationException($"Initializer '{name}' is not a float32 tensor.");
                }

                using var source = torch.tensor(typedTensor.Value.ToArray(), typedTensor.Shape, dtype: ScalarType.Float32, device: target.device);
                target.detach().copy_(source);
            }

            private static void LoadLongTensor(
                IReadOnlyDictionary<string, Onnxify.OnnxTensor> tensors,
                string name,
                Tensor target
            )
            {
                if (!tensors.TryGetValue(name, out var tensor))
                {
                    throw new KeyNotFoundException($"The ONNX model does not contain initializer '{name}'.");
                }

                if (tensor is not Onnxify.OnnxTensor<long> typedTensor)
                {
                    throw new InvalidOperationException($"Initializer '{name}' is not an int64 tensor.");
                }

                using var source = torch.tensor(typedTensor.Value.ToArray(), typedTensor.Shape, dtype: ScalarType.Int64, device: target.device);
                target.detach().copy_(source);
            }

            private static void LoadTensor<T>(
                IReadOnlyDictionary<string, Onnxify.OnnxTensor> tensors,
                string name,
                Tensor target,
                ScalarType scalarType
            )
            {
                if (!tensors.TryGetValue(name, out var tensor))
                {
                    throw new KeyNotFoundException($"The ONNX model does not contain initializer '{name}'.");
                }

                if (tensor is not Onnxify.OnnxTensor<T> typedTensor)
                {
                    throw new InvalidOperationException($"Initializer '{name}' is not a {typeof(T).Name} tensor.");
                }

                var values = typedTensor.Value.ToArray();
                using var source = scalarType switch
                {
                    ScalarType.Byte => torch.tensor((byte[])(object)values, typedTensor.Shape, dtype: ScalarType.Byte, device: target.device),
                    ScalarType.Int8 => torch.tensor((sbyte[])(object)values, typedTensor.Shape, dtype: ScalarType.Int8, device: target.device),
                    ScalarType.Int16 => torch.tensor((short[])(object)values, typedTensor.Shape, dtype: ScalarType.Int16, device: target.device),
                    ScalarType.Int32 => torch.tensor((int[])(object)values, typedTensor.Shape, dtype: ScalarType.Int32, device: target.device),
                    ScalarType.Int64 => torch.tensor((long[])(object)values, typedTensor.Shape, dtype: ScalarType.Int64, device: target.device),
                    ScalarType.Float32 => torch.tensor((float[])(object)values, typedTensor.Shape, dtype: ScalarType.Float32, device: target.device),
                    ScalarType.Float64 => torch.tensor((double[])(object)values, typedTensor.Shape, dtype: ScalarType.Float64, device: target.device),
                    ScalarType.Bool => torch.tensor((bool[])(object)values, typedTensor.Shape, dtype: ScalarType.Bool, device: target.device),
                    _ => throw new NotSupportedException($"Unsupported Torch tensor data type for ONNX initializer import: {scalarType}."),
                };
                target.detach().copy_(source);
            }

            private static void LoadFloatTensorTransposed2D(
                IReadOnlyDictionary<string, Onnxify.OnnxTensor> tensors,
                string name,
                Tensor target
            )
            {
                if (!tensors.TryGetValue(name, out var tensor))
                {
                    throw new KeyNotFoundException($"The ONNX model does not contain initializer '{name}'.");
                }

                if (tensor is not Onnxify.OnnxTensor<float> typedTensor || typedTensor.Shape.Length != 2)
                {
                    throw new InvalidOperationException($"Initializer '{name}' is not a 2D float32 tensor.");
                }

                using var source = torch.tensor(typedTensor.Value.ToArray(), typedTensor.Shape, dtype: ScalarType.Float32, device: target.device).transpose(0, 1).contiguous();
                target.detach().copy_(source);
            }

            private static void LoadOnnxLstmWeights(
                IReadOnlyDictionary<string, Onnxify.OnnxTensor> tensors,
                string wName,
                string rName,
                string? bName,
                TorchModules.LSTM module,
                long hiddenSize,
                long inputSize,
                long numDirections
            )
            {
                var w = GetFloatTensor(tensors, wName);
                var r = GetFloatTensor(tensors, rName);
                var b = bName is null ? null : GetFloatTensor(tensors, bName);
                var state = module.state_dict();

                for (var direction = 0L; direction < numDirections; direction++)
                {
                    var suffix = direction == 1L ? "_reverse" : string.Empty;
                    CopyOnnxLstmGateMatrix(
                        w,
                        direction,
                        hiddenSize,
                        inputSize,
                        GetRequiredStateTensor(state, $"weight_ih_l0{suffix}")
                    );
                    CopyOnnxLstmGateMatrix(
                        r,
                        direction,
                        hiddenSize,
                        hiddenSize,
                        GetRequiredStateTensor(state, $"weight_hh_l0{suffix}")
                    );

                    if (b is not null)
                    {
                        CopyOnnxLstmGateVector(
                            b,
                            direction,
                            hiddenSize,
                            offset: 0L,
                            GetRequiredStateTensor(state, $"bias_ih_l0{suffix}")
                        );
                        CopyOnnxLstmGateVector(
                            b,
                            direction,
                            hiddenSize,
                            offset: 4L * hiddenSize,
                            GetRequiredStateTensor(state, $"bias_hh_l0{suffix}")
                        );
                    }
                }
            }

            private static Onnxify.OnnxTensor<float> GetFloatTensor(
                IReadOnlyDictionary<string, Onnxify.OnnxTensor> tensors,
                string name
            )
            {
                if (!tensors.TryGetValue(name, out var tensor))
                {
                    throw new KeyNotFoundException($"The ONNX model does not contain initializer '{name}'.");
                }

                return tensor as Onnxify.OnnxTensor<float>
                    ?? throw new InvalidOperationException($"Initializer '{name}' is not a float32 tensor.");
            }

            private static Tensor GetRequiredStateTensor(
                IReadOnlyDictionary<string, Tensor> state,
                string name
            )
            {
                if (!state.TryGetValue(name, out var tensor) || tensor is null || tensor.IsInvalid)
                {
                    throw new KeyNotFoundException($"The TorchSharp LSTM state does not contain tensor '{name}'.");
                }

                return tensor;
            }

            private static void CopyOnnxLstmGateMatrix(
                Onnxify.OnnxTensor<float> source,
                long direction,
                long hiddenSize,
                long width,
                Tensor target
            )
            {
                var data = source.Value.ToArray();
                var gateBlockLength = hiddenSize * width;
                var reordered = ReorderOnnxLstmGateData(
                    data,
                    direction,
                    gateBlockLength,
                    directionStride: 4L * gateBlockLength,
                    offset: 0L
                );
                using var tensor = torch.tensor(reordered, new long[] { 4L * hiddenSize, width }, dtype: ScalarType.Float32, device: target.device);
                target.detach().copy_(tensor);
            }

            private static void CopyOnnxLstmGateVector(
                Onnxify.OnnxTensor<float> source,
                long direction,
                long hiddenSize,
                long offset,
                Tensor target
            )
            {
                var data = source.Value.ToArray();
                var reordered = ReorderOnnxLstmGateData(
                    data,
                    direction,
                    gateBlockLength: hiddenSize,
                    directionStride: 8L * hiddenSize,
                    offset
                );
                using var tensor = torch.tensor(reordered, new long[] { 4L * hiddenSize }, dtype: ScalarType.Float32, device: target.device);
                target.detach().copy_(tensor);
            }

            private static float[] ReorderOnnxLstmGateData(
                float[] source,
                long direction,
                long gateBlockLength,
                long directionStride,
                long offset
            )
            {
                var directionOffset = (direction * directionStride) + offset;
                var result = new float[checked((int)(4L * gateBlockLength))];

                CopyGateBlock(source, result, directionOffset + 0L * gateBlockLength, 0L * gateBlockLength, gateBlockLength);
                CopyGateBlock(source, result, directionOffset + 2L * gateBlockLength, 1L * gateBlockLength, gateBlockLength);
                CopyGateBlock(source, result, directionOffset + 3L * gateBlockLength, 2L * gateBlockLength, gateBlockLength);
                CopyGateBlock(source, result, directionOffset + 1L * gateBlockLength, 3L * gateBlockLength, gateBlockLength);
                return result;
            }

            private static void CopyGateBlock(
                float[] source,
                float[] target,
                long sourceOffset,
                long targetOffset,
                long length
            )
            {
                Array.Copy(source, checked((int)sourceOffset), target, checked((int)targetOffset), checked((int)length));
            }
        }
        """;
    }

    private static string BuildTorchForwardBody(
        ModelGenerationSpecification specification,
        TorchModuleGenerationSpecification torchModule,
        ImmutableArray<TorchForwardGroupSpecification> forwardGroups
    )
    {
        var values = new Dictionary<string, string>(StringComparer.Ordinal);
        values[torchModule.InputOnnxName] = torchModule.InputParameterName;
        foreach (var initializer in torchModule.Initializers)
        {
            values[initializer.OnnxName] = initializer.FieldName;
        }

        var statements = new List<string>();
        var modulesByNodeName = torchModule.ModuleNodes.ToDictionary(static x => x.NodeName, StringComparer.Ordinal);
        var groupsByStartNodeName = forwardGroups.ToDictionary(static x => x.Nodes[0].Name, StringComparer.Ordinal);
        var groupedNodeNames = GetGroupedTorchNodeNames(forwardGroups);

        foreach (var node in torchModule.Nodes)
        {
            if (groupedNodeNames.Contains(node.Name))
            {
                continue;
            }

            if (groupsByStartNodeName.TryGetValue(node.Name, out var group))
            {
                var input = values[group.Nodes[0].Inputs[0]];
                var groupOutput = group.OutputOnnxName;
                var groupLocalName = ToCamelIdentifier(groupOutput, "value");
                values[groupOutput] = groupLocalName;
                statements.Add($"var {groupLocalName} = {group.FieldName}.forward({input});");
                continue;
            }

            var expression = modulesByNodeName.TryGetValue(node.Name, out var module)
                ? EmitTorchModuleNode(module, node, values)
                : EmitTorchNode(node, values);
            var output = node.Outputs[0];
            var localName = ToCamelIdentifier(output, "value");
            values[output] = localName;
            statements.Add($"var {localName} = {expression};");
        }

        if (!values.TryGetValue(torchModule.OutputOnnxName, out var outputExpression))
        {
            outputExpression = ToCamelIdentifier(torchModule.OutputOnnxName, "output");
        }

        statements.Add($"return {outputExpression};");
        return string.Join("\n", statements);
    }

    private static HashSet<string> GetGroupedModuleNodeNames(
        ImmutableArray<TorchForwardGroupSpecification> forwardGroups
    )
    {
        return new HashSet<string>(
            forwardGroups
                .SelectMany(static group => group.Nodes)
                .Select(static node => node.Name),
            StringComparer.Ordinal
        );
    }

    private static HashSet<string> GetGroupedTorchNodeNames(
        ImmutableArray<TorchForwardGroupSpecification> forwardGroups
    )
    {
        return new HashSet<string>(
            forwardGroups
                .SelectMany(static group =>
                    group.ResidualAddNode is null
                        ? group.Nodes.Skip(1).Select(static node => node.Name)
                        : group.Nodes.Skip(1).Select(static node => node.Name).Concat([group.ResidualAddNode.Name])),
            StringComparer.Ordinal
        );
    }

    private static ImmutableArray<TorchForwardGroupSpecification> BuildForwardGroups(
        TorchModuleGenerationSpecification torchModule
    )
    {
        var moduleNodeNames = new HashSet<string>(
            torchModule.ModuleNodes.Select(static x => x.NodeName),
            StringComparer.Ordinal
        );

        var inputUseCounts = torchModule.Nodes
            .SelectMany(static x => x.Inputs)
            .GroupBy(static x => x, StringComparer.Ordinal)
            .ToDictionary(static x => x.Key, static x => x.Count(), StringComparer.Ordinal);

        var groups = ImmutableArray.CreateBuilder<TorchForwardGroupSpecification>();
        var index = 0;

        while (index < torchModule.Nodes.Length)
        {
            var node = torchModule.Nodes[index];
            if (!moduleNodeNames.Contains(node.Name))
            {
                index++;
                continue;
            }

            var groupNodes = ImmutableArray.CreateBuilder<TorchNodeSpecification>();
            groupNodes.Add(node);
            var cursor = index;
            while (cursor + 1 < torchModule.Nodes.Length)
            {
                var current = torchModule.Nodes[cursor];
                var next = torchModule.Nodes[cursor + 1];
                if (!moduleNodeNames.Contains(next.Name)
                    || next.Inputs.Length == 0
                    || current.Outputs.Length != 1
                    || !string.Equals(next.Inputs[0], current.Outputs[0], StringComparison.Ordinal)
                    || !inputUseCounts.TryGetValue(current.Outputs[0], out var useCount)
                    || useCount != 1)
                {
                    break;
                }

                groupNodes.Add(next);
                cursor++;
            }

            if (groupNodes.Count >= 2)
            {
                var immutableGroupNodes = groupNodes.ToImmutable();
                var residualAddNode = TryGetResidualAddNode(
                    torchModule.Nodes,
                    cursor + 1,
                    immutableGroupNodes[0].Inputs[0],
                    immutableGroupNodes[immutableGroupNodes.Length - 1].Outputs[0]
                );

                var kind = residualAddNode is null
                    ? TorchForwardGroupKind.Sequential
                    : TorchForwardGroupKind.Residual;

                var name = kind == TorchForwardGroupKind.Sequential
                    ? $"ForwardBlock{groups.Count}"
                    : $"ResidualBlock{groups.Count}";

                groups.Add(
                    new TorchForwardGroupSpecification(
                        name,
                        "_" + ToCamelIdentifier(name, "block"),
                        $"{name}Module",
                        kind,
                        immutableGroupNodes,
                        residualAddNode,
                        residualAddNode?.Outputs[0] ?? immutableGroupNodes[immutableGroupNodes.Length - 1].Outputs[0]
                    )
                );
                index = residualAddNode is null ? cursor + 1 : cursor + 2;
                continue;
            }

            index++;
        }

        return groups.ToImmutable();
    }

    private static TorchNodeSpecification? TryGetResidualAddNode(
        ImmutableArray<TorchNodeSpecification> nodes,
        int index,
        string inputName,
        string branchOutputName
    )
    {
        if (index >= nodes.Length)
        {
            return null;
        }

        var node = nodes[index];
        if (node.OpType != "Add" || node.Inputs.Length != 2)
        {
            return null;
        }

        var leftIsResidual = string.Equals(node.Inputs[0], inputName, StringComparison.Ordinal)
            && string.Equals(node.Inputs[1], branchOutputName, StringComparison.Ordinal);
        var rightIsResidual = string.Equals(node.Inputs[1], inputName, StringComparison.Ordinal)
            && string.Equals(node.Inputs[0], branchOutputName, StringComparison.Ordinal);

        return leftIsResidual || rightIsResidual ? node : null;
    }

    private static string BuildForwardGroupTypes(
        ImmutableArray<TorchForwardGroupSpecification> forwardGroups,
        TorchModuleGenerationSpecification torchModule
    )
    {
        if (forwardGroups.Length == 0)
        {
            return string.Empty;
        }

        var modulesByNodeName = torchModule.ModuleNodes.ToDictionary(static x => x.NodeName, StringComparer.Ordinal);
        return string.Join(
            "\n\n",
            forwardGroups.Select(group =>
            {
                var modules = group.Nodes.Select(node => modulesByNodeName[node.Name]).ToImmutableArray();
                var fields = string.Join(
                    "\n",
                    modules.Select(static module => $"private readonly {module.FieldTypeName} {module.FieldName};")
                );
                var initializers = string.Join(
                    "\n",
                    modules.Select(static module => $"{module.FieldName} = {module.ConstructorExpression};")
                        .Concat(["RegisterComponents();"])
                );
                var loadStatements = string.Join(
                    "\n",
                    modules.SelectMany(static module => module.LoadStatements)
                );
                var forwardStatements = new List<string>();
                var current = "input";
                foreach (var node in group.Nodes)
                {
                    var module = modulesByNodeName[node.Name];
                    var localName = ToCamelIdentifier(node.Outputs[0], "value");
                    forwardStatements.Add($"var {localName} = {module.FieldName}.forward({current});");
                    current = localName;
                }

                forwardStatements.Add(group.Kind == TorchForwardGroupKind.Residual
                    ? $"return input + {current};"
                    : $"return {current};");

                return $$"""
                private sealed class {{group.TypeName}} : torch.nn.Module<Tensor, Tensor>
                {
                    {{Indent(fields, 1)}}

                    public {{group.TypeName}}() : base(nameof({{group.TypeName}}))
                    {
                        {{Indent(initializers, 2)}}
                    }

                    public void LoadWeights(IReadOnlyDictionary<string, Onnxify.OnnxTensor> tensors)
                    {
                        {{Indent(loadStatements, 2)}}
                    }

                    public override Tensor forward(Tensor input)
                    {
                        {{Indent(string.Join("\n", forwardStatements), 2)}}
                    }
                }
                """;
            })
        );
    }

    private static string EmitTorchModuleNode(
        TorchModuleNodeSpecification module,
        TorchNodeSpecification node,
        IReadOnlyDictionary<string, string> values
    )
    {
        var input = values[node.Inputs[0]];
        if (module.ForwardExpression is not null)
        {
            return string.Format(System.Globalization.CultureInfo.InvariantCulture, module.ForwardExpression, input);
        }

        return module.Kind == TorchModuleNodeKind.Linear && module.TransposeInput
            ? $"{module.FieldName}.forward({input}.transpose(0, 1))"
            : $"{module.FieldName}.forward({input})";
    }

    private static string EmitTorchNode(
        TorchNodeSpecification node,
        IReadOnlyDictionary<string, string> values
    )
    {
        if (!_inlineOperators.TryGetValue(node.OpType, out var @operator))
        {
            throw new NotSupportedException($"Unsupported TorchModule node op '{node.OpType}'.");
        }

        return @operator.Emit(node, values);
    }

